# Basic if elif else statements 
# if statement basic code
a = 10
b = 20
if a<b:
    print(f'{a} less than {b}')

# elif statement example
a = 10
b = 20
if a>b:
    print(f'{a} greater than {b}')
elif a<b:
    print(f"{a} less than {b}")
    
    
# else statement 
a = 10
b = 20
if a>10:
    print("yes")
else:
    print("no")
