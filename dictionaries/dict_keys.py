'''
Dictionary keys must be immutable like(str, int, bool, or tuple).

Dictionaries keys examples:- 

'''
string = {
    'str':'value1',
    'str2':'value2'
    }
    
print(string)

integer = {
    23:'age',
    1772832:'salary'
}

print(integer)

boolean = {
    True : 'salary hike',
    False: 'unprecedented'
}
print(boolean)

tuple1 = {
    (1, 'python') : 'example for tuple immutable',
    (2, 'mysql'):'good'
}

print(tuple1)




