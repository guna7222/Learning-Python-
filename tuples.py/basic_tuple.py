# A tuple is a collection which is ordered, unchangeable, and allow duplicate values.
# create a tuple
this_tuple = ('mango', "apple", 'orange')
print(this_tuple)
print(type(this_tuple))

# Tuple items are indexed, index starts from [0] position. 
# Tuple allows duplicate values.

this_tuple = ('mango', "apple", 'orange', "orange") # Orange is a duplicate value 
print(this_tuple)

# Tuple len() function is used to check the length of the tuple

this_tuple = ('mango', "apple", 'orange', "orange") # Orange is a duplicate value 
print(len(this_tuple))

