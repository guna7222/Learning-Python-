# what is list?
'''
List is a collection of differnt data types and list is used to store multipule values in a single variable.
list items are ordered, allows duplicates, mutable.
list item index starts from 0
list are create using sqaure brackets.
'''
# Creating list 
x = ["python", 100, 90.9] # square brackets are used to create a list 
print(x)
print(type(x))

# list constructor
a = list(("python", 100, 90.9))
print(a)
print(type(a))

#List items can be of any data type:
x = ["python", 100, 90.9, True]

#len() function is used to get the length of the list 
x = ["python", 100, 90.9]
print(len(x))



          
          
    
