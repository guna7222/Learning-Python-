# Join a list 
# One of the easiest way to join a list using + arthmetic operator 
guna = ['python', 'Django', 'git', 'mysql']
thrilok = ['html', 'css', 'docker', 'aws']
combine = guna + thrilok
print(combine)

# another Example
guna = ['python', 'Django', 'git', 'mysql']
thrilok = ['html', 'css', 'docker', 'aws']
for i in guna:
    thrilok.append(i)
    print(thrilok)