# to check whether a specified sequence item is present or not in a list using 'in membership operator'
fruits = ["apple", "banana", "orange"]
for i in fruits:
    if "orange" in i:
        print("Yes, it is in fruits")
    else:
        print("no, it is not avaliable in fruits")   
